import math
from dataclasses import dataclass
from enum import StrEnum, auto
from typing import Self

PAGE_ROWS = 4
PAGE_COLS = 2
PAGE_SIZE = PAGE_ROWS * PAGE_COLS


@dataclass
class Word:
    class Category(StrEnum):
        ACTIONS = (auto(), 6)
        ADJECTIVES = (auto(), 8)
        BATTLE = (auto(), 2)
        CONDITIONS = (auto(), 5)
        ENDINGS = (auto(), 4)
        EVENTS = (auto(), 9)
        FEELINGS = (auto(), 5)
        GREETINGS = (auto(), 2)
        HOBBIES = (auto(), 7)
        LIFESTYLE = (auto(), 6)
        MISC = (auto(), 8)
        MOVE_1 = (auto(), 9)
        MOVE_2 = (auto(), 10)
        PEOPLE = (auto(), 3)
        POKEMON = (auto(), 0)
        POKEMON_2 = (auto(), 10)
        SPEECH = (auto(), 4)
        STATUS = (auto(), 1)
        TIME = (auto(), 7)
        TRAINER = (auto(), 1)
        VOICES = (auto(), 3)

        scroll_distance: int

        def __new__(cls, name: str, scroll_distance: int) -> Self:
            self = str.__new__(cls)
            self._value_ = name
            self.scroll_distance = scroll_distance
            return self

    text: str
    index: int
    category: Category
    scroll_distance: int

    @classmethod
    def new(cls, text: str, index: int, category: Category, pos_in_category: int):
        scroll_distance = category.scroll_distance + cls._scroll_distance(
            pos_in_category
        )
        return cls(text, index, category, scroll_distance)

    @staticmethod
    def _scroll_distance(pos: int) -> int:
        return pos // PAGE_SIZE + math.ceil((pos % PAGE_SIZE) / PAGE_COLS)


